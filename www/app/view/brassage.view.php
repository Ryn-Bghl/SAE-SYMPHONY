     <div class="text">
       <h2>Le processus de BRASSAGE</h2>
       <p>La préparation des ingrédients, c’est à dire du malt, orges, houblons, l’eau, de la levure et des gousses de vanilles.</p>
     </div>
   </section>

   <hr>

   <section class="step reverse">
     <div class="text">
       <h2>CONCASSAGE</h2>
       <p>Broyer les grains de malt pour en briser l’enveloppe sans les réduire en poudre. Cela facilite l’extraction des sucres pendant l’empâtage. Une mouture bien réalisée optimise le rendement de la bière.</p>
     </div>
     <img src="../../public/images/concassage.webp" alt="Concassage malt">
   </section>

   <hr>

   <section class="step">
     <img src="../../public/images/emphatage.webp" alt="Ingrédients bière">
     <div class="text">
       <h2>EMPATHAGE</h2>
       <p>Mélanger le malt concassé avec de l’eau chaude pour activer les enzymes. Ces enzymes transforment l’amidon des grains en sucres fermentescibles. La température est contrôlée pour influencer le profil de la bière. Cett étape produit le moût sucré nécessair à la fermentation.</p>
     </div>
   </section>

   <hr>

   <section class="step reverse">
     <div class="text">
       <h2>RINCAGE</h2>
       <p>Extraire un maximum de sucres restés dans les grains après l’empâtage. On verse de l’eau chaude sur la drêche pour récupérer ces sucres. Cela augmente le rendement sans trop diluer le moût. C’est une étape importante avant la cuisson.</p>
     </div>
     <img src="../../public/images/rincage.webp" alt="Concassage malt">
   </section>

